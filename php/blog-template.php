                        <div class="mc-blog-single-section__text-wrapper mc-text-wrapper">
                            <p><i>Обычно у маленького ребенка преобладает не логическое, а образное мышление. Через создание образов малыш не просто самовыражается, но и готовит благодатную почву для последующего интеллектуального роста. О благотворном влиянии рисования на детей знал еще Аристотель. Он утверждал, что этот вид творчества делает ребенка более гармоничным и способствует его разностороннему развитию.</i></p>
                            <p>К сожалению, современные родители редко осознают, что так называемая креативность формируется еще в дошкольном возрасте с помощью активной творческой деятельности. Вместо того, чтобы дать малышам выразить себя, например, через рисунок, они чуть ли не с пеленок пытаются развивать в них механическую память и логику.</p>
                            <p>Рисуя свои шедевры, дети генерируют образы, находят ассоциативные связи между предметами, учатся открывать в них новые качества и рассматривать с разных сторон. Даже когда ребенок становится взрослым, принесенная из детства способность творчески мыслить и постоянно создавать что-то новое никуда не девается. Она остается с человеком навсегда и неизбежно влияет на все сферы его жизни.</p>
                            <h2 id="mc-blog-single-h3-1">Что дают детям уроки рисования</h2>
                            <p>Отдавая ребенка в художественную школу, важно понимать, что главная ценность уроков живописи заключается не в том, какие картинки получатся на выходе, а в самом творческом процессе. Что же происходит с малышом во время рисования?</p>
                            <ul>
                                <li><b>Работают оба полушария.</b> Благодаря развитию мелкой моторики стимулируются различные участки мозга, которые отвечают за мышление, речь, память, координацию. Возникают устойчивые связи между правым и левым полушариями.</li>
                                <li><b>Ориентация в пространстве.</b> В процессе рисования ребенок начинает понимать, как соотносятся между собой предметы, как они проецируются на плоскости. Развивается пространственное мышление, способность сравнивать и обобщать.</li>
                                <li><b>Повышается самооценка.</b> Малыш, который рисует, чувствует себя создателем, творцом. Он сам решает, какой будет его картина, что на ней важно изобразить и в каких тонах. Это формирует лидерские качества, повышает самооценку.</li>
                                <li><b>Всегда есть результат.</b> Ребенок привыкает работать не просто так, а целенаправленно, ориентируясь на конкретный результат — рисунок, который необходимо продумать до мелочей и довести до совершенства.</li>
                            </ul>
                            <h2 id="mc-blog-single-h3-2">Гении, которых воспитала живопись</h2>
                            <p><b>Пол Маккартни.</b> Мало кто знает, что легендарный рок-музыкант с детства увлекался живописью. Он умел “схватывать образы” не только в музыке, но и на бумаге. Пол очень любил рисовать, но безумно боялся, что люди могут неправильно воспринять его творчество. Через много лет рисунки Пола Маккартни, случайно найденные на чердаке, будут высоко оценены критиками и выставлены на аукцион.</p>
                            <p><b>Джим Керри</b> — еще один яркий пример увлеченного рисованием гения. Потрясающий актер и сценарист, один из самых высокооплачиваемых комиков в мире. “Я очень часто рисую. Это моя большая страсть, которой я одержим еще с детства”, — говорит Джим. С детства дом будущего артиста был доверху забит картинами, они были во всех комнатах, в каждом уголке. Даже сейчас, в перерывах между съемками, Джим Керри продолжает писать свои полотна.</p>
                            <p><b>Тарас Шевченко.</b> И, напоследок, вспомним нашего выдающегося поэта-кобзаря Тараса Шевченко. Его тянуло к живописи с необычайной силой. Он убегал из дома, находил себе учителей-маляров, тайком рисовал по ночам, пока его хозяин не переехал в Петербург и не отдал его учиться к известному живописцу. Далее выкуп из крепостничества, учеба в Петербургской академии живописи, тесное взаимодействие с Карлом Брюлловым… В итоге, Шевченко так и не стал известным художником, но зато вырос в великого поэта — его художественные образы переросли в литературные.</p>
                            <p><i>Таким образом, чтобы полноценно реализоваться во взрослой жизни, нужно с детства быть творческой личностью — живописцем, создателем, творцом. Поощряя ребенка заниматься рисованием, мы даем ему гораздо больше, чем возможность когда-то стать художником. Мы даем ему шанс стать Человеком…</i></p>
