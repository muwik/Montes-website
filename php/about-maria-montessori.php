        <div class="container-xxl mc-container-mw">
            <h1 class="mc-heading-t">О Марии Монтессори</h1>
            <div class="mc-maria-section__info">
                <div class="img-wrapper">
                    <picture>
                        <source type="image/webp" srcset="images/Mmontessori.webp">
                        <img src="images/Mmontessori.jpg" alt="test">
                    </picture>
                </div>
                <div class="mc-maria-section__text-wrapper mc-text-wrapper">
                    <p><a href="https://ru.wikipedia.org/wiki/%D0%9C%D0%BE%D0%BD%D1%82%D0%B5%D1%81%D1%81%D0%BE%D1%80%D0%B8,_%D0%9C%D0%B0%D1%80%D0%B8%D1%8F" target="_blank">Мария Монтессори</a> — выдающийся итальянский педагог, врач и философ, автор педагогической системы, основанной на идее свободного воспитания ребенка.</p>
                    <p>Мария Монтессори родилась в 1870 году в Италии. Она была пионером в области образования и одной из первых женщин в Италии, получивших медицинское образование. После успешной карьеры в медицине Мария Монтессори перешла к работе в образовательной области.</p>
                    <p>Метод Монтессори основывается на идее самостоятельного обучения. Мария Монтессори верила, что дети наиболее эффективно учатся, когда они вовлечены в процесс и интересуются тем, что делают. Этот метод подчеркивает значимость сенсорного развития, самостоятельности и роли подготовленной образовательной среды в учебном процессе.</p>
                    <p>Методы Марии Монтессори оказали глубокое влияние на образовательную систему во всем мире. Ее идеи о самостоятельном обучении, подготовленной среде и сенсорном развитии продолжают формировать подходы к обучению и развитию детей.</p>
                    <p>Одним из свидетельств международного признания Марии Монтессори стало решение ЮНЕСКО в 1988 г., касающееся всего четырёх педагогов, определивших способ педагогического мышления в XX веке: американца Джона Дьюи, немца Георга Кершенштейнера, итальянки Марии Монтессори и украинского педагога Антона Макаренко.</p>
